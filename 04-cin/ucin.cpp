#include <iostream>
using namespace std;

int main()
{
    int a;
    float b;
    cout << "Enter a int number: ";
    cin >> a;
    cout << "You entered: " << a << endl;
    cout << "Enter a float number: ";
    cin >> b;
    cout << "You entered: " << b << endl;
    cout << "The sum of the two numbers is: " << a + b << endl;

    return 0;
}